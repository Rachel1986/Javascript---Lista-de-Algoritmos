// Dado o tamanho do lado de um quadrado, calcular a área e o perímetro do mesmo.

let ladoQuadrado = Number(prompt("Digite o tamanho do lado de um quadrado."));

let area = ladoQuadrado * ladoQuadrado;
alert("A área do seu quadrado é " + area);

let perimetro = ladoQuadrado * 4;
alert("O perimetro do seu quadrado é " + perimetro);