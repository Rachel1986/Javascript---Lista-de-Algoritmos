//Dado os três lados de um triângulo determinar o perímetro do mesmo.

let lado1 = Number(prompt("Digite o lado 1 de um triângulo."));
let lado2 = Number(prompt("Digite o lado 2 do triângulo"));
let lado3 = Number(prompt("Digite o lado 3 do triângulo"));

let perimetro = lado1 + lado2 + lado3;

alert("O perímetro do triângulo é " + perimetro);