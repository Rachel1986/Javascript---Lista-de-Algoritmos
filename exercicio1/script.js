/* Dado o tamanho da base e da altura de um retângulo, calcular a sua área e o seu
perímetro.*/

let base = Number(prompt("Digite o tamanho da base do retângulo"));

let altura = Number(prompt("Digite o tamanho da altura do retângulo"));

let area = base * altura;
alert("A área do seu retângulo é " + area)

let perimetro = 2 * (base + altura);
alert("O perímetro do seu retângulo é " + perimetro)


      
